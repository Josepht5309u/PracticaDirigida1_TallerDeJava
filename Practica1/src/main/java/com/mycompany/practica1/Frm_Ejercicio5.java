    private void btmTodasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmTodasActionPerformed
        // TODO add your handling code here:
        int numero1 = Integer.parseInt(txtNumero1.getText());
        int numero2 = Integer.parseInt(txtNumero2.getText());
        int suma = numero1 + numero2;
        int resta = numero1 - numero2;
        int multiplicacion = numero1 * numero2;
        int division = numero1 / numero2;
        int residuo = numero1%numero2;
        txtResultados.setText("La suma de los 2 numeros es: "+suma+ "\n");
        txtResultados.append("La resta de los 2 numeros es: "+resta+ "\n");
        txtResultados.append("La multiplicaci√≥n de los 2 numeros es: "+multiplicacion+"\n");
        txtResultados.append("La divisi√≥n de los 2 numeros es: "+division+ " con "+ residuo +" de residuo \n");
    }//GEN-LAST:event_btmTodasActionPerformed

